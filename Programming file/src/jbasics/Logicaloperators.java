package jbasics;

public class Logicaloperators {
	public static void main(String[] args) {
// Logical operators
		boolean a=true;
		boolean b= false;
	System.out.println(a&b);
	System.out.println(a&a);
	System.out.println(b&b);
	// this is and (&) operators here both the values must be true
	System.out.println(a|a);
	System.out.println(a|b);
	System.out.println(b|b);
	//this is pipe operator (|) which checks if either one is true or not
	
	
	
	
	
	}
}